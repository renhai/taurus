package me.renhai.taurus.vo;

public class CastingItem {
	
	private Integer celebrityId;
	private String celebrityName;
	private String celebrityImage;
	private String characters;
	
	public Integer getCelebrityId() {
		return celebrityId;
	}
	public void setCelebrityId(Integer celebrityId) {
		this.celebrityId = celebrityId;
	}
	public String getCelebrityName() {
		return celebrityName;
	}
	public void setCelebrityName(String celebrityName) {
		this.celebrityName = celebrityName;
	}
	public String getCharacters() {
		return characters;
	}
	public void setCharacters(String characters) {
		this.characters = characters;
	}
	public String getCelebrityImage() {
		return celebrityImage;
	}
	public void setCelebrityImage(String celebrityImage) {
		this.celebrityImage = celebrityImage;
	}
	
	
}
